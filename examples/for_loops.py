
# for loops use control variables
# automated counting of loops progress


for i in range(5):
    print(i)
    print("Value of i is:",i)

print("---------------------------------------")

for i in range(4, 7):
    print(i)
    print("Value of i is:",i)

print("---------------------------------------")

# range has increments
# from 4, up to 8, in increments of 2
for i in range(4, 8, 2):
    print(i)
    print("Value of i is:",i)