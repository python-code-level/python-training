# break
print("using break")
for i in range(1,10):
    if i == 5:
        break
    print("Loop: ", i)
print("end of loop")

print("--------------------------------------------------------------------")

# continue
print("using break")
for i in range(1,15):
    if i >= 5 and i <=10:
        continue
    print("Loop: ", i)
print("end of loop")

print("-------------------------------------------------------------------")