# escape characters

test = "a string"
test2 = "a \"quoted\" string"
print(test)
print(test2)
print("------------------------------------------------")
test3 = "also a 'quoted' string"
test4 = 'still a "quoted" string'
print(test3)
print(test4)
test5 = "i'm a string"
test6 = 'i\'m a string'
print(test5)
print(test6)
# strings can be empty
test7 = ""
print(test7)
